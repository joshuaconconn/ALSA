package com.example.thesis.Class;

public class ProgramExerciseClass {
    String part, name, coachProgId, id, set, reps, weight, url, alternative, alternativeURL;


    public ProgramExerciseClass(){

    }

    public String getAlternative() {
        return alternative;
    }

    public void setAlternative(String alternative) {
        this.alternative = alternative;
    }

    public String getAlternativeURL() {
        return alternativeURL;
    }

    public void setAlternativeURL(String alternativeURL) {
        this.alternativeURL = alternativeURL;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getPart() {
        return part;
    }

    public void setPart(String part) {
        this.part = part;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCoachProgId() {
        return coachProgId;
    }

    public void setCoachProgId(String coachProgId) {
        this.coachProgId = coachProgId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSet() {
        return set;
    }

    public void setSet(String set) {
        this.set = set;
    }

    public String getReps() {
        return reps;
    }

    public void setReps(String reps) {
        this.reps = reps;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }
}
